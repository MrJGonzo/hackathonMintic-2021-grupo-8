import com.google.firebase.auth.AuthResult;
import com.google.firebase.auth.FirebaseAuth;

public class Registro extends AppCompatActivity {

    private FirebaseAuth mAuth ;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_registro);

        mAuth = FirebaseAuth.getInstance();
        mAuth.createUserWithEmailAndPassword("i@i.com", "123456").addOnCompleteListener(new OnCompleteListener<AuthResult>() {
            @Override
            public void onComplete(@NonNull Task<AuthResult> task) {
                if(task.isSuccessful()){
                    Toast.makeText(getApplicationContext(), "Guardo usuario con Ã©xito en DB", Toast.LENGTH_LONG).show() ;
                }else{
                    Toast.makeText(getApplicationContext(), "Error al guardar en la BD", Toast.LENGTH_LONG).show() ;
                }
            }
        });

    }
}